import UIKit

extension UIColor {
    
    @nonobjc class var appBackGround: UIColor {
        return UIColor(red: 237/255, green: 237/255, blue: 237/255, alpha: 1)
    }
    
    @nonobjc class var appGray: UIColor {
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1)
    }
    
    @nonobjc class var backGrounTextField: UIColor {
        return UIColor(red: 48/255, green: 48/255, blue: 51/255, alpha: 1)
    }
}
